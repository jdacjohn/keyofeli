			<td valign="top"><h3 style="margin:0; padding: 0px;">W.G. SNUFFY WALDEN  - Music By</h3>
				<p>
					Born in Louisiana, raised in Texas, W.G. Snuffy Walden started piano lessons at the age of six before deciding after a year that the self-taught method was the better choice.  While attending college in Houston on a double major of pre-med and math, he worked at a late-night radio show and played guitar in a strip club.  Then one day he dropped out of school, quit his job and strapped on his guitar full-time.  "I went from being a wannabe doctor to a rock musician.  Hey, it was the sixties; it was a different time."  In 1968, he formed a blues-based rock trio, Stray Dog, who relocated from Texas to England.  Snuffy recorded several albums and then relocated to Los Angeles where he continued his solo career and began touring with artists such as Chaka Khan, Eric Burdon and Donna Summer.
					</p>

			<p>	It was during this period that a couple of film and television agents heard Snuffy playing guitar during regular monthly gigs at a Santa Monica nightclub.  "When they asked me about scoring for film and television, I wasn’t sure what it entailed," Snuffy confesses, "but I could see the handwriting on the wall for touring, and it wasn’t pretty.  I kept envisioning Holiday Inn at age 60."
</p>
			<p>	Snuffy later found out that the only reason he got the interview with the producers of "thirtysomething" was because they wanted to see the guy with a name like "Snuffy."  Regardless, he submitted a tape.   "I never heard a word so I assumed they hated it.  They were about to sign another composer when they popped the audio and video cassettes in and loved the music and the way it worked with the show.  That was  ‘thirtysomething’, the first show I did."  Snuffy scored the pilot, the series and wrote the theme.
</p>
			<p>	A month and a half after "thirtysomething" went on the air, he got a call asking if he would be interested in a "little show" premiering after the Super Bowl.  The show turned out to be "The Wonder Years".  Snuffy scored the pilot, then went on to score the series, and revamped a Beatles song for the end credits.
</p>
				<p>Snuffy went on to score "Ellen", "Roseanne", "Sisters", "The Jackie Thomas Show," and many other television shows; more than a dozen cable films and movies for television; several independent films; and the major release "Leaving Normal."  He has received twelve Emmy nominations and was awarded the Emmy for Outstanding Main Title Theme for "The West Wing". 
			</p>

				</td>
