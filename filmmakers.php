			<td valign="top"><h3 style="margin:0; padding: 0px;">PATTIE KELLY - Producer</h3>
				<p>Originally from New Mexico, Pattie Kelly has always been involved in the arts, equally comfortable behind the 
					scenes as well as an accomplished musician and performer.  After graduating with a Bachelor’s Degree in Film and 
					Television Production and a Minor in Music and Theatre from West Texas A&M, Ms. Kelly was transplanted to 
					Los Angeles where she began working in television production, eventually becoming a producer for many commercials 
					and industrials for companies such as Ford and Proctor and Gamble.  Recently, she has produced fund raising 
					commercials on location in Brazil and Indonesia for the international children’s charity, ChildFund.
</p>
			<p>	Ms. Kelly produced the critically acclaimed world premiere of <em>"The Abbey Yard"</em> at the Odyssey Theatre in Los Angeles.  
				A period drama set in early twentieth century Ireland, she also co-wrote, produced and performed on the play’s original 
				soundtrack.  This production of <em>"The Abbey Yard"</em> was the first pairing of Scarpaci and Kelly as a producing and directing 
				team and would ultimately become the catalyst that launched their enduring partnership and Scarpaci/Kelly Productions.
</p>
			<p>	With Scarpaci, she created and developed the whimsical world of the Beyond the Page Café, a children’s space 
				that was the basis for the award winning Disney children’s series <em>"Beyond the Page"</em>.   In addition to producing the 
				series, Ms. Kelly also wrote the first three episodes of <em>"Beyond the Page"</em>.   Also for Disney, Scarpaci/Kelly has 
				just completed the third episode for the award winning children’s series,<em>"Safety Smart Science"</em>  with Bill Nye the Science Guy.  
</p>
			<p>	Feature films include <em>"Welcome to September"</em> which won the Cine Golden Eagle, Berkeley Film Festival, 
				Swansea Bay Film Festival and Mania Film Festival and is distributed worldwide by York Entertainment. 
				 <em>"Blue Bus"</em> won Memphis International Film Festival, Macon Film Festival, River Bend Film Festival and 
				Swansea Bay Film Festival in Wales. 
</p>
				<p>Ms. Kelly is also the CEO of Scarpaci/Kelly Productions.
