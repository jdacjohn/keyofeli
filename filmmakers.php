			<td valign="top"><h3 style="margin:0; padding: 0px;">PHIL SCARPACI - Director</h3>
								<p>Phil Scarpaci started his directorial career working at the Group Repertory Theatre in Los Angeles and since those early days has helmed award winning endeavors that include feature films, children’s programming, commercials and theater.
								</p>
								<p>Prior to this year’s just completed, "In The Key of Eli", Mr. Scarpaci’s last film, "Blue Bus" (2009), won Best International Feature at the Swansea Bay International Film Festival, Wales. It also won Best Feature Film at the Memphis International Film Festival and was a winner at the Macon Film Festival.  His previous feature "Welcome to September" had an award-winning run on the Indie film festival circuit as well. September is distributed by York Entertainment.
								</p>
								<p>Mr. Scarpaci has been directing award winning Disney Programming for over 20 years. In 2009 he garnered the prestigious IDMA/DVDA Excellence Awards for Excellence in Children’s Programming
								for his latest Disney project, "Bill Nye the Science Guy" and is currently in post production on a new Bill Nye project.  
								</p>
								<p>Mr. Scarpaci’s diverse directing background has one thing in common:  his ability to establish a creative and collaborative work environment. It’s not uncommon for actors to say Phil’s approach to directing promotes some of "my best work ever" and that his feel for the material and his touch with actors are what make it so special to work with him.
