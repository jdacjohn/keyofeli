		<td valign="top"><h3 style="margin:0; padding: 0px;">STUART ASBJORNSEN - Director of Photography</h3>
			<p>Stuart and director Phil Scarpaci have been friends and worked together for many years. This familiarity 
				was especially helpful during the production of "In The Key of Eli" since Phil’s time was split, not only 
				directing but also playing a character in the film.
</p>
			<p>Stuart has shot many documentaries as well as working for extended periods of time in episodic television which 
				has provided him with considerable practice in working quickly and efficiently.
</p>
			<p><em>"In The Key of Eli"</em> used a variety of locations and had a pretty good-sized cast.  There were at least 12 distinct 
				locations ranging from the port of LA to the hills of Ventura County and 10 cast members for a shoot only 15 days 
				long. Evocative scenes of the character, Eli, were made with modern film stock cut down to super 8 dimension, and 
				shot with a camera Stuart has owned since he was fourteen!  Director Phil Scarpaci and Stuart tried to make the 
				most of each location and include a lot of visual texture in each scene.  
</p>
			<p>Collaborating with Phil and Producers Pattie Kelly and Darren Anderson on <em>"In The Key of Eli"</em> as well as a 
				previous film, <em>"Blue Bus"</em>, made for very challenging, exciting and rewarding adventures. Stuart considers them 
				both really excellent films and is very proud to have played a part in their production.
		</p>
			

