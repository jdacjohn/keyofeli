			<td valign="top"><h3 style="margin:0; padding: 0px;">DARREN ANDERSON - Producer</h3>
			<p>	Darren Anderson was born in Portsmouth, England.  Even though from a very young age Darren 
				found himself playing the lead roles in many school and later college plays he never quite felt comfortable 
				in front of the camera.  There was an interest and excitement Darren felt in the process of putting a performance 
				together.  After leaving college with qualifications in Art, Design, Media Studies and production Media Darren decided 
				to move to the states to figure out where his passion for media would lead him.</p> 

<p>
				Darren’s first job in television would be as a production assistant for an infomercial company based just outside 
				Los Angeles, California.  He quickly realized this was a perfect opportunity to be around and learn the creative 
				and logistical process of production from the ground up.  It was there Darren was introduced to his two mentors 
				Pattie Kelly and Phil Scarpaci who would guide him through all aspects of television production.  With a thirst 
				for knowledge and a lot of hard work he soon found himself climbing the ladder in the production world first as a 
				production coordinator then production manager and then line producer.  In the following years Darren would be 
				involved with many different types of production including commercials, music videos, educational programming and 
				independent feature films.</p>  

			<p>	<em>"In the Key of Eli"</em> is Darren Anderson’s second feature film as a producer.</p>

				


